    private void btnIntroducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntroducirActionPerformed
     double altura=Double.parseDouble(txtAltura.getText());
      float r=Float.parseFloat(txtRadio.getText());
      circulo = new Circulo( r );
      double x1=Double.parseDouble(txtX1.getText());
      double x2=Double.parseDouble(txtX2.getText());
      double y1=Double.parseDouble(txtY1.getText());
      double y2=Double.parseDouble(txtY2.getText());
      linea= new Linea(y1,x1,y2,x2);
      double base=Double.parseDouble(txtBase.getText());
      rectangulo= new Rectangulo(10,150,base,altura);
    }//GEN-LAST:event_btnIntroducirActionPerformed

