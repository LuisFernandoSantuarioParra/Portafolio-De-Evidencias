    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
       texCirculo.setText(String.valueOf(circulo.area()));
       texRectangulo.setText(String.valueOf(rectangulo.area()));
       texLongitud.setText(String.valueOf(linea.longitud()));
     CirculoGrafico cg = new CirculoGrafico(10,250,circulo.getR(),Color.BLACK);
     RectanguloGrafico rg = new RectanguloGrafico(10,150,(int)rectangulo.getBase(),(int)rectangulo.getAltura(),Color.BLUE);
     LineaGrafica lg= new LineaGrafica(10,100, (int)linea.longitud(), 100,Color.red);
     //cg.dibujar(jPanel2.getGraphics(), 10, 250, (int)circulo.getR(),(int)circulo.getR());
     cg.dibujar(jPanel2.getGraphics());
     rg.dibujar(jPanel2.getGraphics());
     lg.dibujar(jPanel2.getGraphics());
    }//GEN-LAST:event_btnMostrarActionPerformed

