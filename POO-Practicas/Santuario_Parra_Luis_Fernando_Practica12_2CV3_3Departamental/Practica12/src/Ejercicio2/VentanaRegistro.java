    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     Control co=new Control();
     
        String nombre=txt6.getText();
        n=co.new Nombrar(nombre);
        
        String clave=txt1.getText();
        double num=Double.parseDouble(clave);
        c=co.new Clave(num);
        String titulo=txt2.getText();
        t=co.new Titulo(titulo);
        
        String autor=txt3.getText();
        au=co.new Autor(autor);
        
        String editorial=txt4.getText();
        e=co.new Editorial(editorial);
     try {
         a.crearArchivo(n, t, e, au, c);
     } catch (FileNotFoundException ex) {
         Logger.getLogger(VentanaRegistro.class.getName()).log(Level.SEVERE, null, ex);
     }
    }//GEN-LAST:event_jButton1ActionPerformed

